# -*- coding: utf-8 -*-
numero_uno = 20;
numero_dos = 10;

resultado = numero_uno + numero_dos;
print("Suma", resultado);

resultado = numero_uno - numero_dos;
print("Rest", resultado);

resultado = numero_uno * numero_dos;
print("Multiplicacion", resultado);

resultado = numero_uno / numero_dos; #retorna numero flotante
print("Division", resultado);

resultado = numero_uno // numero_dos; #retorna numero entero
print("Division", resultado);

resultado = numero_uno ** numero_dos; #retorna numero exponencial
print("Exponencial", resultado);